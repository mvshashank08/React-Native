import React, { Component } from 'react';
import {
  AppRegistry,
  StyleSheet,
  Text,
  View,
  TextInput
} from 'react-native';
import QRCode from 'react-native-qrcode';
export default class QRGenApp extends Component {
	state = {
    text: 'http://facebook.github.io/react-native/',
  };
  render() {
    return (
      <View style={styles.container}>
        <Text style={styles.welcome}>
          QR code generator
        </Text>
        <TextInput
          style={{width: 280}}
          onChangeText={(text) => this.setState({text: text})}
          value={this.state.text}
        />
        <QRCode
          value={this.state.text}
          size={200}
          bgColor='purple'
          fgColor='white'/>
      </View>
    );
  }
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
    justifyContent: 'center',
    alignItems: 'center',
    backgroundColor: '#F5FCFF',
  },
  welcome: {
    fontSize: 20,
    textAlign: 'center',
    margin: 10,
  },
  instructions: {
    textAlign: 'center',
    color: '#333333',
    marginBottom: 5,
  },
});

AppRegistry.registerComponent('QRGenApp', () => QRGenApp);
